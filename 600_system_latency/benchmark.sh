#!/bin/bash

# Function to get architecture
get_arch() {
  arch=$(uname -m)
  if [[ "$arch" == "x86_64" ]]; then
    echo "Intel/AMD (x86_64)"
  elif [[ "$arch" == "aarch64" ]]; then
    echo "Arm (aarch64)"
  else
    echo "Unknown architecture: $arch"
  fi
}

# Display system information
echo "=== System Information ==="
echo "Architecture: $(get_arch)"
echo "CPU Model:"
lscpu | grep "Model name"
echo ""

# Compile and run system latency benchmark
echo "Compiling system latency benchmark..."
gcc -O2 latency_benchmark.c -o latency_benchmark
echo "Running system latency benchmark..."
./latency_benchmark

echo "System latency benchmark complete!"

